cells.append(md("## 1. Parse + map ids + split (shared with the MF notebook)"))
cells.append(code(
"def parse_combined(path, chunksize=CHUNK_ROWS):\n"
"    us, ms, rs, ds = [], [], [], []\n"
"    cur = 0\n"
"    for chunk in pd.read_csv(path, header=None, names=['user', 'rating', 'date'],\n"
"                             dtype={'user': str}, chunksize=chunksize):\n"
"        is_hdr = chunk['rating'].isna()\n"
"        mv = pd.to_numeric(chunk['user'].where(is_hdr).str.rstrip(':'),\n"
"                           errors='coerce').ffill().fillna(cur)\n"
"        rows = ~is_hdr\n"
"        us.append(pd.to_numeric(chunk.loc[rows, 'user']).to_numpy(np.int32))\n"
"        ms.append(mv[rows].to_numpy(np.int16))\n"
"        rs.append(chunk.loc[rows, 'rating'].to_numpy(np.int8))\n"
"        ds.append(chunk.loc[rows, 'date'].to_numpy('datetime64[D]'))\n"
"        cur = int(mv.iloc[-1])\n"
"    return (np.concatenate(us), np.concatenate(ms),\n"
"            np.concatenate(rs), np.concatenate(ds))\n"
"\n"
"if os.path.exists(CACHE_NPZ):\n"
"    z = np.load(CACHE_NPZ)\n"
"    raw_user, movie, rating, date = z['user'], z['movie'], z['rating'], z['date']\n"
"    print(f'loaded cache: {len(rating):,} ratings')\n"
"else:\n"
"    pu, pm, pr, pd_ = [], [], [], []\n"
"    for n in SUBSAMPLE_FILES:\n"
"        u, m, r, d = parse_combined(os.path.join(DATA_DIR, f'combined_data_{n}.txt'))\n"
"        pu.append(u); pm.append(m); pr.append(r); pd_.append(d)\n"
"    raw_user = np.concatenate(pu); movie = np.concatenate(pm)\n"
"    rating = np.concatenate(pr); date = np.concatenate(pd_)\n"
"    np.savez(CACHE_NPZ, user=raw_user, movie=movie, rating=rating, date=date)\n"
"    print(f'parsed & cached {len(rating):,} ratings')"))

cells.append(code(
"uniq_users, user = np.unique(raw_user, return_inverse=True)\n"
"user = user.astype(np.int32); del raw_user\n"
"movie = (movie - 1).astype(np.int16)\n"
"if USER_FRAC < 1.0:\n"
"    keep = np.zeros(len(uniq_users), bool)\n"
"    keep[rng.choice(len(uniq_users), int(len(uniq_users) * USER_FRAC), replace=False)] = True\n"
"    mrows = keep[user]\n"
"    user, movie, rating, date = user[mrows], movie[mrows], rating[mrows], date[mrows]\n"
"    _, user = np.unique(user, return_inverse=True); user = user.astype(np.int32)\n"
"n_users  = int(user.max()) + 1\n"
"n_movies = int(movie.max()) + 1\n"
"print(f'{len(rating):,} ratings | {n_users:,} users | {n_movies:,} movies')\n"
"\n"
"titles = pd.read_csv(os.path.join(DATA_DIR, 'movie_titles.csv'),\n"
"                     header=None, encoding='latin-1', on_bad_lines='skip',\n"
"                     names=['movie_id', 'year', 'title'], usecols=[0, 1, 2])\n"
"title_of = dict(zip(titles['movie_id'].astype(int), titles['title']))"))

cells.append(code(
"order = np.lexsort((date, user))\n"
"su = user[order]\n"
"_, starts, counts = np.unique(su, return_index=True, return_counts=True)\n"
"starts = starts.astype(np.int32); counts = counts.astype(np.int32)\n"
"pos    = np.arange(len(order), dtype=np.int32) - np.repeat(starts, counts)\n"
"n_test = np.where(counts >= 5, np.ceil(counts * TEST_FRAC).astype(np.int32), 0)\n"
"thresh = np.repeat(counts - n_test, counts)\n"
"is_test = np.zeros(len(order), bool)\n"
"is_test[order] = pos >= thresh\n"
"del order, su, pos, thresh, starts, counts\n"
"tr = ~is_test\n"
"u_tr, m_tr, r_tr = user[tr], movie[tr], rating[tr]\n"
"u_te, m_te, r_te = user[is_test], movie[is_test], rating[is_test]\n"
"print(f'train {tr.sum():,}  |  test {is_test.sum():,}')"))

cells.append(md("## 2. Baseline biases (centering + comparison point)"))
cells.append(code(
"def fit_biases(u, m, r, n_u, n_m, reg=10.0, n_iter=10):\n"
"    mu = r.mean()\n"
"    bu = np.zeros(n_u, np.float32); bi = np.zeros(n_m, np.float32)\n"
"    uc = np.bincount(u, minlength=n_u); ic = np.bincount(m, minlength=n_m)\n"
"    for _ in range(n_iter):\n"
"        bi = np.bincount(m, weights=r - mu - bu[u], minlength=n_m) / (ic + reg)\n"
"        bu = np.bincount(u, weights=r - mu - bi[m], minlength=n_u) / (uc + reg)\n"
"    return mu, bu, bi\n"
"\n"
"mu, bu, bi = fit_biases(u_tr, m_tr, r_tr, n_users, n_movies)\n"
"def predict_baseline(u, m):\n"
"    return np.clip(mu + bu[u] + bi[m], 1, 5)\n"
"def rmse(pred, actual):\n"
"    return float(np.sqrt(np.mean((pred - actual) ** 2)))\n"
"print('baseline test RMSE:', rmse(predict_baseline(u_te, m_te), r_te))"))

cells.append(md(
"## 3. Item-item similarity\n"
"\n"
"Cosine similarity on **baseline residuals** `r - (μ + b_u + b_i)` (removes user/item\n"
"offsets so similarity reflects *taste*, not popularity). Power users are capped at\n"
"`RCAP` ratings for this step — a few users rate thousands of movies and dominate the\n"
"`Σ_u d_u²` cost. Similarity is shrunk by co-rating count `n_ij/(n_ij+SHRINK)` so pairs\n"
"with few common users aren't trusted. Top-`K` neighbors per item are kept; the full\n"
"17.7k×17.7k matrix is never materialized (computed in blocks)."))
cells.append(code(
"resid_tr = (r_tr - predict_baseline(u_tr, m_tr)).astype(np.float32)\n"
"\n"
"# user profiles sorted by user (used for BOTH capping and prediction)\n"
"ords = np.argsort(u_tr, kind='stable')\n"
"su = u_tr[ords]\n"
"ub = np.searchsorted(su, np.arange(n_users + 1))      # CSR-style row bounds\n"
"prof_items = m_tr[ords].astype(np.int32)\n"
"prof_resid = resid_tr[ords]\n"
"\n"
"# cap power users (drop random surplus rows beyond RCAP) for the similarity matrix\n"
"counts = ub[1:] - ub[:-1]\n"
"mask = np.ones(len(u_tr), bool)\n"
"for uidx in np.where(counts > RCAP)[0]:\n"
"    s, e = ub[uidx], ub[uidx + 1]\n"
"    drop = s + rng.choice(e - s, (e - s) - RCAP, replace=False)\n"
"    mask[drop] = False\n"
"us_c, ms_c, rs_c = su[mask], prof_items[mask], prof_resid[mask]\n"
"print(f'similarity uses {len(us_c):,} of {len(u_tr):,} ratings (cap {RCAP}/user)')\n"
"\n"
"R  = csr_matrix((rs_c, (us_c, ms_c)), shape=(n_users, n_movies))\n"
"Bn = csr_matrix((np.ones(len(rs_c), np.float32), (us_c, ms_c)), shape=(n_users, n_movies))\n"
"col_norm = np.sqrt(np.asarray(R.multiply(R).sum(0)).ravel()) + 1e-8\n"
"Rt, Bt = R.T.tocsr(), Bn.T.tocsr()"))
cells.append(code(
"nbr    = np.zeros((n_movies, K_NEIGHBORS), np.int32)\n"
"nbrsim = np.zeros((n_movies, K_NEIGHBORS), np.float32)\n"
"t0 = time.time()\n"
"for start in range(0, n_movies, SIM_BLOCK):\n"
"    end = min(start + SIM_BLOCK, n_movies)\n"
"    num = (Rt[start:end] @ R).toarray()              # resid . resid\n"
"    cc  = (Bt[start:end] @ Bn).toarray()             # co-rating counts\n"
"    denom = col_norm[start:end, None] * col_norm[None, :]\n"
"    sim = (num / denom) * (cc / (cc + SHRINK))       # cosine x shrinkage\n"
"    sim[np.arange(end - start), np.arange(start, end)] = -np.inf   # drop self\n"
"    part = np.argpartition(-sim, K_NEIGHBORS, axis=1)[:, :K_NEIGHBORS]\n"
"    psim = np.take_along_axis(sim, part, axis=1)\n"
"    o = np.argsort(-psim, axis=1)\n"
"    nbr[start:end]    = np.take_along_axis(part, o, axis=1)\n"
"    nbrsim[start:end] = np.take_along_axis(psim, o, axis=1)\n"
"nbrsim[~np.isfinite(nbrsim)] = 0.0\n"
"nbrsim[nbrsim < 0] = 0.0                              # keep positive correlations\n"
"print(f'similarity done in {time.time()-t0:.0f}s | mean |neighbors|>0 per item: '\n"
"      f'{(nbrsim > 0).sum(1).mean():.1f}')"))

cells.append(md(
"## 4. Neighborhood prediction\n"
"\n"
"`r̂_ui = baseline_ui + (Σ_{j∈N(i), rated by u} s_ij·resid_uj) / (Σ s_ij + DENOM_REG)`.\n"
"\n"
"The `DENOM_REG` term is **denominator shrinkage**: without it, a candidate whose only\n"
"rated neighbors carry tiny similarity mass gets a wild weighted-average residual and\n"
"the correction *hurts* (raw kNN scored worse than the baseline). Adding `DENOM_REG`\n"
"pulls low-confidence corrections toward 0, so kNN beats the baseline (tuned: ~0.925→\n"
"0.915 on a subsample). Vectorized per user with a reusable buffer — cost is\n"
"`Σ_u (d_u + |candidates_u|·K)`, not `n_users·n_movies`."))
cells.append(code(
"_buf_resid = np.zeros(n_movies, np.float32)\n"
"_buf_rated = np.zeros(n_movies, bool)\n"
"\n"
"def predict_knn(u_arr, m_arr):\n"
"    u_arr = np.asarray(u_arr); m_arr = np.asarray(m_arr)\n"
"    base = predict_baseline(u_arr, m_arr)\n"
"    out = base.astype(np.float32).copy()\n"
"    o = np.argsort(u_arr, kind='stable')\n"
"    uu = u_arr[o]\n"
"    gusers, gstart = np.unique(uu, return_index=True)\n"
"    gend = np.append(gstart[1:], len(uu))\n"
"    for g, uidx in enumerate(gusers):\n"
"        rows = o[gstart[g]:gend[g]]            # positions in the original arrays\n"
"        items = prof_items[ub[uidx]:ub[uidx + 1]]\n"
"        resid = prof_resid[ub[uidx]:ub[uidx + 1]]\n"
"        _buf_resid[items] = resid; _buf_rated[items] = True\n"
"        C = m_arr[rows]\n"
"        nb = nbr[C]; ns = nbrsim[C]            # (|C| x K)\n"
"        rated = _buf_rated[nb]\n"
"        numv = (ns * _buf_resid[nb] * rated).sum(1)\n"
"        denv = (ns * rated).sum(1)             # ns already >= 0\n"
"        corr = numv / (denv + DENOM_REG)       # denom shrinkage damps small-mass corrections\n"
"        out[rows] = np.clip(base[rows] + corr, 1, 5)\n"
"        _buf_resid[items] = 0; _buf_rated[items] = False\n"
"    return out"))

cells.append(md("## 5. Evaluation — RMSE + MAP@10 (same metrics as MF notebook)"))
cells.append(code(
"def map_at_k(u_te, m_te, r_te, predict_fn, k=10, max_users=20000, seed=SEED):\n"
"    pred = predict_fn(u_te, m_te)\n"
"    o = np.argsort(u_te, kind='stable')\n"
"    uu, rr, pp = u_te[o], r_te[o], pred[o]\n"
"    users_u, st = np.unique(uu, return_index=True); en = np.append(st[1:], len(uu))\n"
"    rgn = np.random.default_rng(seed); sel = np.arange(len(users_u))\n"
"    if len(sel) > max_users: sel = rgn.choice(sel, max_users, replace=False)\n"
"    aps = []\n"
"    for j in sel:\n"
"        s, e = st[j], en[j]\n"
"        if e - s < 2: continue\n"
"        rel = rr[s:e] >= RELEVANT_THRESHOLD; R = int(rel.sum())\n"
"        if R == 0: continue\n"
"        rank = np.argsort(-pp[s:e], kind='stable')[:k]\n"
"        hits = 0; ap = 0.0\n"
"        for i, ri in enumerate(rank, 1):\n"
"            if rel[ri]: hits += 1; ap += hits / i\n"
"        aps.append(ap / min(k, R))\n"
"    return float(np.mean(aps)), len(aps)\n"
"\n"
"def map_at_k_sampled(predict_fn, u_tr, m_tr, u_te, m_te, r_te,\n"
"                     k=10, n_neg=100, max_users=10000, seed=SEED):\n"
"    rgn = np.random.default_rng(seed)\n"
"    o = np.argsort(u_tr, kind='stable'); su, sm = u_tr[o], m_tr[o]\n"
"    bounds = np.searchsorted(su, np.arange(n_users + 1))\n"
"    ot = np.argsort(u_te, kind='stable'); tu, tm, trr = u_te[ot], m_te[ot], r_te[ot]\n"
"    tusers, tst = np.unique(tu, return_index=True); ten = np.append(tst[1:], len(tu))\n"
"    sel = np.arange(len(tusers))\n"
"    if len(sel) > max_users: sel = rgn.choice(sel, max_users, replace=False)\n"
"    aps = []\n"
"    for j in sel:\n"
"        uidx = int(tusers[j]); s, e = tst[j], ten[j]\n"
"        t_movies, t_r = tm[s:e], trr[s:e]\n"
"        rel = t_r >= RELEVANT_THRESHOLD; R = int(rel.sum())\n"
"        if R == 0: continue\n"
"        seen = set(sm[bounds[uidx]:bounds[uidx + 1]].tolist()) | set(t_movies.tolist())\n"
"        negs = []\n"
"        while len(negs) < n_neg:\n"
"            for c in rgn.integers(0, n_movies, n_neg):\n"
"                c = int(c)\n"
"                if c not in seen:\n"
"                    negs.append(c); seen.add(c)\n"
"                    if len(negs) >= n_neg: break\n"
"        cand = np.concatenate([t_movies, np.array(negs, dtype=np.int32)])\n"
"        labels = np.concatenate([rel, np.zeros(len(negs), bool)])\n"
"        scores = predict_fn(np.full(len(cand), uidx, np.int32), cand)\n"
"        rank = np.argsort(-scores, kind='stable')[:k]\n"
"        hits = 0; ap = 0.0\n"
"        for i, ri in enumerate(rank, 1):\n"
"            if labels[ri]: hits += 1; ap += hits / i\n"
"        aps.append(ap / min(k, R))\n"
"    return float(np.mean(aps)), len(aps)"))
cells.append(code(
"t0 = time.time()\n"
"pred_knn_te = predict_knn(u_te, m_te)      # full test (the heavy call, ~1-2 min)\n"
"print(f'kNN test RMSE: {rmse(pred_knn_te, r_te):.4f}  ({time.time()-t0:.0f}s)')\n"
"\n"
"# reuse precomputed preds for held-out MAP (avoid recomputing); sampled-neg needs live calls\n"
"s_ho, n_ho = map_at_k(u_te, m_te, r_te, lambda u, m: pred_knn_te)\n"
"s_sn, n_sn = map_at_k_sampled(predict_knn, u_tr, m_tr, u_te, m_te, r_te)\n"
"print(f'kNN MAP@10  held-out={s_ho:.4f} ({n_ho})  sampled-neg={s_sn:.4f} ({n_sn})')\n"
"s_b_ho, _ = map_at_k(u_te, m_te, r_te, predict_baseline)\n"
"s_b_sn, _ = map_at_k_sampled(predict_baseline, u_tr, m_tr, u_te, m_te, r_te)\n"
"print(f'baseline MAP@10  held-out={s_b_ho:.4f}  sampled-neg={s_b_sn:.4f}')"))

